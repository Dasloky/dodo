import streamlit as st

# הגדרות דף (מופיע בלשונית של הדפדפן)
st.set_page_config(page_title="המתנה שלי", page_icon="🎁")

# כותרת ראשית
st.title("היי! איזה כיף שסרקת את הברקוד ✨")

# טקסט חופשי
st.write("""
ברוך הבא לאפליקציה המיוחדת שבניתי עבורך.  
כאן אפשר להוסיף ברכות, זיכרונות או כל דבר אחר!
""")

# הוספת תמונה (שימי קובץ בשם 'photo.jpg' באותה תיקייה)
# אם אין לך תמונה עדיין, אפשר לשים את השורה הזו בהערה
try:
    st.image("photo.jpg", caption="רגע למזכרת", use_container_width=True)
except:
    st.info("כאן תופיע תמונה ברגע שתעלי קובץ בשם photo.jpg ל-GitHub")

# כפתור אינטראקטיבי
if st.button("לחץ כאן להפתעה 🎈"):
    st.balloons()
    st.success("מזל טוב! (או כל הודעה אחרת שתרצי)")

# אפשר להוסיף עוד המון דברים בקלות!